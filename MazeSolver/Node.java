//Assignment: Assignment 11, Node.java
//Description: Contains the code that creates the node class for the coordinates

public class Node {
	//Instance variables
	private int x;
	private int y;
	
	public Node(int num1, int num2) {
		x = num1;
		y = num2;
	}
	
	//Returns x value
	public int getX() {
		return x;
	}//End of getX
	
	//Returns y value
	public int getY() {
		return y;
	}//End of getY
}
